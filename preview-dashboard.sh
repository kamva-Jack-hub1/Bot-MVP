echo "Uncommitted changes:"
git status --short 2>/dev/null || echo "  (git not available in current context)"
echo ""

# Team output section
echo "👥 TEAM OUTPUT STATUS:"
total_files=0
for agent in otis mara silas cleo; do
    if [ -d "team-outputs/$agent" ]; then
        count=$(find "team-outputs/$agent" -type f \( -name "*.md" -o -name "*.json" -o -name "*.py" -o -name "*.csv" -o -name "*.txt" \) 2>/dev/null | wc -l)
